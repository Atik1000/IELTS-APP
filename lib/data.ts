export type QuizQuestion = {
  id: string;
  wordId: string;
  question: string;
  options: string[];
  correctIndex: number;
};

// Generate quiz questions from words (first 5 words)
export function getQuizQuestions(words: WordItem[], count: number = 5): QuizQuestion[] {
  const selected = words.slice(0, Math.min(count, words.length));
  return selected.map((w, i) => ({
    id: `q-${w.id}`,
    wordId: w.id,
    question: `What does "${w.word}" mean?`,
    options: shuffle([w.meaning, ...getWrongOptions(w.meaning)]).slice(0, 4),
    correctIndex: 0, // will be set after shuffle
  })).map((q) => {
    const correct = q.options[0];
    const shuffled = shuffle([...q.options]);
    const correctIndex = shuffled.indexOf(correct);
    return { ...q, options: shuffled, correctIndex };
  });
}

function getWrongOptions(correctMeaning: string): string[] {
  const others = PLACEHOLDER_WORDS.filter((w) => w.meaning !== correctMeaning).map((w) => w.meaning);
  return shuffle(others).slice(0, 3);
}

function shuffle<T>(arr: T[]): T[] {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

